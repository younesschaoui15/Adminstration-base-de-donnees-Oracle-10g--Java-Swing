    public void updatelistuser(JList j) {
        try {

            DefaultListModel listModel = new <String>DefaultListModel();
            Statement stmt = null;

            stmt = cn.createStatement();

            ResultSet rs = stmt.executeQuery("Select username FROM dba_users ");
            while (rs.next()) {
                listModel.addElement(rs.getString(1));
            }
            j.setModel(listModel);

        } catch (SQLException ex) {
            Logger.getLogger(AjoutUtilisateur.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void updatesess(JTable t) {
        DefaultTableModel md = (DefaultTableModel) jTable1.getModel();;
        Statement stmt = null;
        while (md.getRowCount() > 0) {
            md.removeRow(0);
        }
        try {
            stmt = cn.createStatement();

            ResultSet rs = stmt.executeQuery("select SID,SERIAL#,USERNAME,STATUS from V$SESSION where username is not null");
            while (rs.next()) {
                String[] s = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)};
                md.addRow(s);
            }
        } catch (SQLException ex) {

        }
        t.setModel(md);

    }

    public void updatTablePrivic(JTable t) {
        DefaultTableModel md = (DefaultTableModel) jXTable1.getModel();;
        Statement stmt = null;
        while (md.getRowCount() > 0) {
            md.removeRow(0);
        }
//        String user = jList1.getSelectedValue().toString();
        String user = jLabel1.getText();
        try {
            stmt = cn.createStatement();

            ResultSet rs = stmt.executeQuery("SELECT * FROM DBA_SYS_PRIVS WHERE grantee = '" + user + "'");
            while (rs.next()) {
                String[] s = {rs.getString(2), rs.getString(3), rs.getString(1)};
                md.addRow(s);
            }
        } catch (SQLException ex) {

        }
        t.setModel(md);

    }

    public void roles() {
        jCheckBox6.setSelected(false);
        jCheckBox3.setSelected(false);
        jCheckBox1.setSelected(false);
        Statement stmt = null;
        String user = jList1.getSelectedValue().toString();
        try {
            stmt = cn.createStatement();

            ResultSet rs = stmt.executeQuery("select * from DBA_ROLE_PRIVS where grantee = '" + user + "'");
            while (rs.next()) {
                String role = rs.getString(2);
                if (role.equals("CONNECT")) {
                    jCheckBox6.setSelected(true);
                }
                if (role.equals("RESOURCE")) {
                    jCheckBox3.setSelected(true);
                }
                if (role.equals("DBA")) {
                    jCheckBox1.setSelected(true);
                }

            }
        } catch (SQLException ex) {

        }
    }
