    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        MySwingUtilities.setContentPane(parent_pane, new modifUser());
        try {

            Statement stmt = null;
            String user = jList1.getSelectedValue().toString();
            stmt = cn.createStatement();

            ResultSet rs = stmt.executeQuery("Select * FROM dba_users where USERNAME = UPPER('" + user + "')");
            while (rs.next()) {
                lbnom.setText(rs.getString(1));
                lbtmpdef.setText(rs.getString(7));
                lbtbtemp.setText(rs.getString(8));
                jComboBox2.addItem(rs.getString(10));
                String stat = rs.getString(4);
                System.out.println(stat);
                if (stat.equals("OPEN")) {
                    lbexper.setVisible(true);
                    check.setVisible(true);
                    jLabel5.setVisible(false);
                    combooo.setSelectedIndex(0);
                }
                if (stat.equals("EXPIRED")) {
                    lbexper.setVisible(false);
                    check.setVisible(false);
                    jLabel5.setVisible(true);
                    combooo.setSelectedIndex(0);
                }
                if (stat.equals("LOCKED")) {
                    combooo.setSelectedIndex(1);
                }
                if (stat.equals("EXPIRED & LOCKED")) {
                    lbexper.setVisible(false);
                    check.setVisible(false);
                    jLabel5.setVisible(true);
                    combooo.setSelectedIndex(1);
                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(AjoutUtilisateur.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

