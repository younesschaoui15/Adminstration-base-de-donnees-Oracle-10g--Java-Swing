    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            if (jList1.getSelectedIndex() < 0) {
                JOptionPane.showMessageDialog(this, "Sélectionnez un UTILISATEUR !!\nClik OK à Continuer!!", "Erreur !!", JOptionPane.WARNING_MESSAGE);
            } else {
                JOptionPane.showConfirmDialog(this, "Vous Voulez Vraiment attribué ce(s) Rôle(s) ?");
                String user = jList1.getSelectedValue().toString();
                String priva = "GRANT CONNECT TO " + user;
                String privd = "GRANT RESOURCE TO " + user;
                String privc = "GRANT DBA TO " + user;

                String privadrop = "REVOKE CONNECT from " + user;
                String privddrop = "REVOKE RESOURCE from " + user;
                String privcdrop = "REVOKE DBA from " + user;
                stmt = cn.createStatement();
                if (jCheckBox6.isSelected()) {
                    stmt.executeUpdate(priva);
                    JOptionPane.showMessageDialog(pga, " Le Rôle <CONNECT> a été bien appliquer a l'utilisateur " + user);
                }
                if (jCheckBox3.isSelected()) {
                    stmt.executeUpdate(privd);
                    JOptionPane.showMessageDialog(pga, " Le Rôle <RESOURCE> a été bien appliquer a l'utilisateur " + user);
                }
                if (jCheckBox1.isSelected()) {
                    stmt.executeUpdate(privc);
                    JOptionPane.showMessageDialog(pga, " Le Rôle <DBA> a été bien appliquer a l'utilisateur " + user);
                }
                if (!jCheckBox6.isSelected()) {
                    stmt.executeUpdate(privadrop);
                    JOptionPane.showMessageDialog(pga, " Le Rôle <CONNECT> a été supprimer de l'utilisateur " + user);
                }
                if (!jCheckBox3.isSelected()) {
                    stmt.executeUpdate(privddrop);
                    JOptionPane.showMessageDialog(pga, " Le Rôle <RESOURCE> a été supprimer de l'utilisateur " + user);
                }
                if (!jCheckBox1.isSelected()) {
                    stmt.executeUpdate(privcdrop);
                    JOptionPane.showMessageDialog(pga, " Le Rôle <DBA> a été supprimer de l'utilisateur " + user);
                }
            }
        } catch (SQLException e) {
            System.out.println("Erreeeur : " + e.getMessage());
        }

    }//GEN-LAST:event_jButton3ActionPerformed

